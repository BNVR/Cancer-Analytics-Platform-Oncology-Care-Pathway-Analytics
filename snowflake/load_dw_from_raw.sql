CREATE OR REPLACE TEMP TABLE STG_PATIENTS AS
SELECT
    PATIENT_ID,
    NAME AS PATIENT_NAME,
    DOB,
    GENDER,
    STAGE,
    BMI,
    SMOKING_STATUS,
    FAMILY_HISTORY_CANCER,
    RADIATION_EXPOSURE,
    PREVIOUS_CANCERS,
    AGE,
    SHA2(CONCAT_WS('||',
        COALESCE(NAME, ''),
        COALESCE(TO_VARCHAR(DOB), ''),
        COALESCE(GENDER, ''),
        COALESCE(STAGE, ''),
        COALESCE(TO_VARCHAR(BMI), ''),
        COALESCE(SMOKING_STATUS, ''),
        COALESCE(FAMILY_HISTORY_CANCER, ''),
        COALESCE(RADIATION_EXPOSURE, ''),
        COALESCE(PREVIOUS_CANCERS, ''),
        COALESCE(TO_VARCHAR(AGE), '')
    ), 256) AS RECORD_HASH
FROM RAW.PATIENTS
QUALIFY ROW_NUMBER() OVER (PARTITION BY PATIENT_ID ORDER BY INGESTED_AT DESC NULLS LAST) = 1;

UPDATE DW.DIM_PATIENTS TARGET
SET VALID_TO = CURRENT_TIMESTAMP(),
    IS_CURRENT = FALSE
FROM STG_PATIENTS SRC
WHERE TARGET.PATIENT_ID = SRC.PATIENT_ID
  AND TARGET.IS_CURRENT = TRUE
  AND TARGET.RECORD_HASH <> SRC.RECORD_HASH;

INSERT INTO DW.DIM_PATIENTS (
    PATIENT_ID,
    PATIENT_NAME,
    DOB,
    GENDER,
    STAGE,
    BMI,
    SMOKING_STATUS,
    FAMILY_HISTORY_CANCER,
    RADIATION_EXPOSURE,
    PREVIOUS_CANCERS,
    AGE,
    RECORD_HASH,
    VALID_FROM,
    VALID_TO,
    IS_CURRENT
)
SELECT
    SRC.PATIENT_ID,
    SRC.PATIENT_NAME,
    SRC.DOB,
    SRC.GENDER,
    SRC.STAGE,
    SRC.BMI,
    SRC.SMOKING_STATUS,
    SRC.FAMILY_HISTORY_CANCER,
    SRC.RADIATION_EXPOSURE,
    SRC.PREVIOUS_CANCERS,
    SRC.AGE,
    SRC.RECORD_HASH,
    CURRENT_TIMESTAMP(),
    NULL,
    TRUE
FROM STG_PATIENTS SRC
LEFT JOIN DW.DIM_PATIENTS CURR
    ON CURR.PATIENT_ID = SRC.PATIENT_ID
   AND CURR.IS_CURRENT = TRUE
WHERE CURR.PATIENT_ID IS NULL
   OR CURR.RECORD_HASH <> SRC.RECORD_HASH;

CREATE OR REPLACE TEMP TABLE STG_CANCER AS
SELECT
    PATIENT_ID,
    PATIENT_NAME,
    CANCER_TYPE,
    ICD_CODE,
    STAGE,
    DESCRIPTION,
    TUMOR_MARKERS,
    SHA2(CONCAT_WS('||',
        COALESCE(PATIENT_NAME, ''),
        COALESCE(CANCER_TYPE, ''),
        COALESCE(ICD_CODE, ''),
        COALESCE(STAGE, ''),
        COALESCE(DESCRIPTION, ''),
        COALESCE(TUMOR_MARKERS, '')
    ), 256) AS RECORD_HASH
FROM RAW.CANCER_CATALOG
QUALIFY ROW_NUMBER() OVER (
    PARTITION BY PATIENT_ID, CANCER_TYPE
    ORDER BY INGESTED_AT DESC NULLS LAST
) = 1;

UPDATE DW.DIM_CANCER_CATALOG TARGET

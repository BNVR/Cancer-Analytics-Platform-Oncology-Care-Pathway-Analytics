TRUNCATE TABLE DW.DIM_DATE;

INSERT INTO DW.DIM_DATE (
    DATE_SK,
    ENCOUNTER_DATE,
    YEAR,
    MONTH,
    DAY,
    QUARTER,
    LOADED_AT
)
SELECT DISTINCT
    TO_NUMBER(TO_CHAR(DATE, 'YYYYMMDD')) AS DATE_SK,
    DATE AS ENCOUNTER_DATE,
    YEAR(DATE) AS YEAR,
    MONTH(DATE) AS MONTH,
    DAY(DATE) AS DAY,
    QUARTER(DATE) AS QUARTER,
    CURRENT_TIMESTAMP()
FROM RAW.ENCOUNTERS
WHERE DATE IS NOT NULL;

TRUNCATE TABLE DW.FACT_ENCOUNTERS;

INSERT INTO DW.FACT_ENCOUNTERS (
    ENCOUNTER_ID,
    PATIENT_SK,
    CANCER_SK,
    PROVIDER_SK,
    PROVIDER_ID,
    DATE_SK,
    PATIENT_ID,
    ENCOUNTER_DATE,
    TREATMENT,
    DRUG_REGIMEN,
    NUMBER_OF_CYCLES,
    TREATMENT_RESPONSE_SCORE,
    OUTCOME,
    BILLING_ID,
    BILLING_AMOUNT,
    INSURANCE,
    PAYMENT_STATUS,
    PIPELINE_RUN_ID,
    LOADED_AT
)
SELECT
    E.ENCOUNTER_ID,
    P.PATIENT_SK,
    C.CANCER_SK,
    PR.PROVIDER_SK,
    PR.PROVIDER_ID,
    D.DATE_SK,
    E.PATIENT_ID,
    E.DATE AS ENCOUNTER_DATE,
    E.TREATMENT,
    E.DRUG_REGIMEN,
    E.NUMBER_OF_CYCLES,
    E.TREATMENT_RESPONSE_SCORE,
    E.OUTCOME,
    B.BILLING_ID,
    B.AMOUNT AS BILLING_AMOUNT,
    B.INSURANCE,
    B.PAYMENT_STATUS,
    E.PIPELINE_RUN_ID,
    CURRENT_TIMESTAMP()
FROM RAW.ENCOUNTERS E
LEFT JOIN DW.DIM_PATIENTS P
    ON E.PATIENT_ID = P.PATIENT_ID
   AND P.IS_CURRENT = TRUE
LEFT JOIN DW.DIM_CANCER_CATALOG C
    ON E.PATIENT_ID = C.PATIENT_ID
   AND C.IS_CURRENT = TRUE
LEFT JOIN (
    SELECT
        PROVIDER_SK,
        PROVIDER_ID,
        ROW_NUMBER() OVER (ORDER BY PROVIDER_ID) - 1 AS PROVIDER_INDEX
    FROM DW.DIM_PROVIDERS
) PR
    ON MOD(ABS(HASH(E.ENCOUNTER_ID)), (SELECT COUNT(*) FROM DW.DIM_PROVIDERS)) = PR.PROVIDER_INDEX
LEFT JOIN DW.DIM_DATE D
    ON E.DATE = D.ENCOUNTER_DATE
LEFT JOIN RAW.BILLING B
    ON E.ENCOUNTER_ID = B.ENCOUNTER_ID;

